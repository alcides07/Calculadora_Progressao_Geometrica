    private void CalcularSn(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalcularSn
       
            try{
                DecimalFormat format = new DecimalFormat("0.00");  
                float a11 = Float.parseFloat (a1.getText());
                int n1 = Integer.parseInt (n.getText());
                float q1 = Float.parseFloat (q.getText());
                float Sn;
                
               if (q1 == -1){
        JOptionPane.showMessageDialog(null, "Confira os dados: Para PG finita, 'q' não pode ser '-1'");
                  }
               if(q1 == 1){
               Sn = n1*a11;       
                   Snn.setText(format.format (Sn));
               }
               if(q1 != 1 && q1 != -1){
                Sn =  (float) (a11*(Math . pow (q1 ,   n1) - 1)/(q1-1));
                Snn.setText(format.format (Sn));
                sn2.setText("S" + n1);
                        }
            }catch(NumberFormatException e){               
                JOptionPane.showMessageDialog(null, "Confira os dados");
            }
           
        

    }//GEN-LAST:event_CalcularSn

